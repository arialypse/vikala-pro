<?php
namespace App\Controllers;

use App\Models\ProductModel;

class ProductController extends BaseController
{
    protected $productModel;

    public function __construct()
    {
        $this->productModel = new ProductModel();
    }

    /**
     * ?????????????????????????????? (?????? Active) ???????????????????????
     */
    public function index()
    {
        $companyId = session()->get('active_company_id');
        if (!$companyId) {
            return redirect()->to('/dashboard')->with('error', '????????????????????????????????');
        }

        $data['products'] = $this->productModel->getActiveProducts($companyId);
        return view('products/index', $data);
    }

    /**
     * ??????????????????????????????????????
     */
    public function store()
    {
        $companyId = session()->get('active_company_id');
        if (!$companyId) {
            return redirect()->to('/dashboard')->with('error', '????????????????????????????????');
        }

        // ???????????????????????????
        $rules = [
            'sku'        => 'required',
            'name'       => 'required',
            'unit_price' => 'required|numeric'
        ];

        if (!$this->validate($rules)) {
            return redirect()->back()->withInput()->with('errors', $this->validator->getErrors());
        }

        // ???????????????????? Database
        $data = [
            'company_id' => $companyId,
            'sku'        => $this->request->getPost('sku'),
            'name'       => $this->request->getPost('name'),
            'unit_price' => $this->request->getPost('unit_price'),
            'is_active'  => 1
        ];

        $this->productModel->insert($data);
        return redirect()->to('/products')->with('success', '???????????????????????????');
    }

    /**
     * ?????????????????????????????? 
     * (???????????????????????????????)
     */
    public function update($id)
    {
        $companyId = session()->get('active_company_id');
        if (!$companyId) {
            return redirect()->to('/dashboard')->with('error', '????????????????????????????????');
        }
        
        // ????????????????????????????????????????? ???????
        $product = $this->productModel->find($id);
        if (!$product || $product['company_id'] != $companyId) {
            return redirect()->to('/products')->with('error', '??????????? ????????????????????????????????');
        }

        $data = [
            'sku'        => $this->request->getPost('sku'),
            'name'       => $this->request->getPost('name'),
            'unit_price' => $this->request->getPost('unit_price')
        ];

        $this->productModel->update($id, $data);
        return redirect()->to('/products')->with('success', '???????????????????????');
    }

    /**
     * ???? Soft Inactivation - ????????????????????
     */
    public function disable($id)
    {
        $companyId = session()->get('active_company_id');
        if (!$companyId) {
            return redirect()->to('/dashboard')->with('error', '????????????????????????????????');
        }
        
        // ???????????????????????????(????)
        $product = $this->productModel->find($id);
        if (!$product || $product['company_id'] != $companyId) {
            return redirect()->to('/products')->with('error', '??????????? ?????????????????????????????');
        }

        // ????????? is_active ???? 0 ????????????????????
        $this->productModel->update($id, ['is_active' => 0]);
        return redirect()->to('/products')->with('success', '??????????????????????????????');
    }

    /**
     * API ??????????????? Auto-complete ????????? (?????????????? JSON)
     */
    public function search()
    {
        $companyId = session()->get('active_company_id');
        if (!$companyId) {
            return $this->response->setJSON([]);
        }

        $keyword = $this->request->getGet('q');
        if (empty($keyword)) {
            return $this->response->setJSON([]);
        }

        $products = $this->productModel->searchProducts($companyId, $keyword);
        return $this->response->setJSON($products);
    }
}
